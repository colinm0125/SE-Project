import org.junit.Test;
import java.util.ArrayList;
import java.util.List;

import static org.junit.Assert.*;

public class PrimeSummationTest {

    @Test
    public void testPrimeSumPerformance() {
        // Define input parameters for the test
        int num = 100; // Adjust the input number as needed
        List<Integer> sums = new ArrayList<>();
        
        // Measure the execution time of the primeSum method
        long startTime = System.currentTimeMillis();
        PrimeSummation.primeSum(num, 1, sums);
        long endTime = System.currentTimeMillis();
        long executionTime = endTime - startTime;
        
        // Print out the execution time
        System.out.println("Execution time for primeSum method with input " + num + ": " + executionTime + " milliseconds");
        
        // Assert that the execution time is within an acceptable range (optional)
        assertTrue("Execution time should be less than 100 milliseconds", executionTime < 100);
    }
}
